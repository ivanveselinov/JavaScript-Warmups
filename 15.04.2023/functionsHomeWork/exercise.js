const squareNumber = function (num1) {
  square = num1 * num1
  console.log(`Result is ${num1} * ${num1} = ${square}`)
}

squareNumber(3)
squareNumber(5)

const halfNumber = function (num1){
  result = num1 / 2

  console.log(`Half number of ${num1} is ${result}`)
}

halfNumber(2)
halfNumber(4)
halfNumber(6)
halfNumber(8)
